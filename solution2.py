'''
2. 現有設定如下：
   s = [1, 2, 3, 4, 5]
   m = [99, 98, 97, 96, 95]
   以 s[0] 對 m[0] 、s[1] 對 m[1] 的方式，依序計算每組的 '+' '*' 運算，
   輸出成 => '1 + 99 = 100', '1 * 99 = 99', '2 + 98 = 100'...
   一條算式為一行
'''
s = [1, 2, 3, 4, 5]
m = [99, 98, 97, 96, 95]
len = len(s)

for i in range(len):
    total = s[i] + m[i]
    mul = s[i] * m[i]
    print(f"{s[i]} + {m[i]} = {total}")
    print(f"{s[i]} * {m[i]} = {mul}")
